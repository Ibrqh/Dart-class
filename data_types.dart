// This is a Dart program that demonstrates the use of various data types such as:
// int, double, String, List, and Map.
void main() {
  // 1. int: An integer number (whole number) without decimals.
  int number = 10;
  print("Integer: $number");

  // 2. double: A number with a decimal point.
  double pi = 3.14;
  print("Double: $pi");

  // 3. String: A sequence of characters. It can contain letters, numbers, and special characters.
  String message = "Hello, World!";
  print("String: $message");

  // 4. List: A collection of elements that can be of the same or different types.
  List<int> numbersList = [1, 2, 3, 4, 5];
  print("List: $numbersList");

  // 5. Map: A collection of key-value pairs where each key is unique.
  Map<String, int> itemsMap = {'apple': 1, 'banana': 2, 'cherry': 3};
  print("Map: $itemsMap");
}
